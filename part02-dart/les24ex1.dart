void main() {
  for (int i = 1; i <= 12; i++) {
    switch (i) {
      case 1:
        print("Январь");
        break;
      case 2:
        print("Февраль");
        break;
      case 3:
        print("Март");
        break;
      case 4:
        print("Апрель");
        break;
      case 5:
        print("Май");
        break;
      case 6:
        print("Июнь");
        break;
      case 7:
        print("Июль");
        break;
      case 8:
        print("Август");
        break;
      case 9:
        print("Сентябрь");
        break;
      case 10:
        print("Октябрь");
        break;
      case 11:
        print("Ноябрь");
        break;
      case 12:
        print("Декабрь");
        break;
      default:
        print("Неверный номер месяца");
    }
  }
}
